# プログラミング言語のバージョン管理とライブラリのバージョン管理を、  
# 別々の環境構築ライブラリで管理する場合と、同じ環境構築ライブラリで管理する場合がある。  
# 
# pythonは複数のバージョンをインストールでき、  
# 標準の仮想環境作成ライブラリvenvでpythonバージョンを指定できるので、  
# 標準のvenvで十分な場合が多い。  
